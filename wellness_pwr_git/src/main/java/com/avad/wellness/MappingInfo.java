package com.avad.wellness;

public class MappingInfo {
	
	String mapping;
	String type;
	String unit;
	
	public MappingInfo(String mapping, String type, String unit) {
		super();
		this.mapping = mapping;
		this.type = type;
		this.unit = unit;
	}
	public String getMapping() {
		return mapping;
	}
	public String getType() {
		return type;
	}
	public String getUnit() {
		return unit;
	}
	
	

}
